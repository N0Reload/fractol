#include "../../inc/fractol.h"

int	motion(int x, int y, t_e *e)
{
	if (e->type == JULIA && boule(e))
	{
		if (x > e->xj)
			e->ci += 0.005;
		if (x < e->xj)
			e->ci -= 0.005;
		if (y > e->yj)
			e->cr += 0.005;
		if (y < e->yj)
			e->cr -= 0.005;
		e->xj = x;
		e->yj = y;
	}
	refresh(e);
	return (0);
}
